import 'package:flutter/cupertino.dart';
import 'package:flutter/material.dart';
import 'question.dart';

void main() => runApp(Quizzler());

class Quizzler extends StatelessWidget {
  @override
  Widget build(BuildContext context) {
    return MaterialApp(
      home: Scaffold(
        backgroundColor: Colors.grey.shade900,
        body: SafeArea(
          child: Padding(
            padding: EdgeInsets.symmetric(horizontal: 10.0),
            child: QuizPage(),
          ),
        ),
      ),
    );
  }
}

class QuizPage extends StatefulWidget {
  @override
  _QuizPageState createState() => _QuizPageState();
}

class _QuizPageState extends State<QuizPage> {

  int count = 0;
  List<Icon> scoreKeeper = [];
    List<Question> listOfQuestions = [
    new Question('You can lead a cow down stairs but not up stairs.', false),
    new Question('Approximately one quarter of human bones are in the feet.', true),
    new Question('A slug\'s blood is green.', true),
  ];

  void verifyAnswer(bool answere) {
    if (answere == listOfQuestions[count].answer) {
      addScore(
        Icon(
          Icons.check,
          color: Colors.green,
        ),
      );
    } else {
      addScore(
        Icon(
          Icons.close,
          color: Colors.red,
        ),
      );
    }
  }

  void addScore(Icon newIcon) {
    setState(() {
      if (count >= 2) {
        count = 0;
        scoreKeeper.clear();
      } else {
        scoreKeeper.add(newIcon);
        count++;
      }
    });
  }

  @override
  Widget build(BuildContext context) {
    return Column(
      mainAxisAlignment: MainAxisAlignment.spaceBetween,
      crossAxisAlignment: CrossAxisAlignment.stretch,
      children: <Widget>[
        Expanded(
          flex: 5,
          child: Padding(
            padding: EdgeInsets.all(10.0),
            child: Center(
              child: Text(
                listOfQuestions[count].question,
                textAlign: TextAlign.center,
                style: TextStyle(
                  fontSize: 25.0,
                  color: Colors.white,
                ),
              ),
            ),
          ),
        ),
        Expanded(
          child: Padding(
            padding: EdgeInsets.all(15.0),
            child: FlatButton(
              textColor: Colors.white,
              color: Colors.green,
              child: Text(
                'True',
                style: TextStyle(
                  color: Colors.white,
                  fontSize: 20.0,
                ),
              ),
              onPressed: () {
                verifyAnswer(true);
              },
            ),
          ),
        ),
        Expanded(
          child: Padding(
            padding: EdgeInsets.all(15.0),
            child: FlatButton(
              color: Colors.red,
              child: Text(
                'False',
                style: TextStyle(
                  fontSize: 20.0,
                  color: Colors.white,
                ),
              ),
              onPressed: () {
                verifyAnswer(false);
              },
            ),
          ),
        ),
        Row(
          children: scoreKeeper,
        )
      ],
    );
  }
}

